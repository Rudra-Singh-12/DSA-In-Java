package Bit_Manipulation;

public class BitsInjava {
    public static void main(String[] args) {
        System.out.println((5 & 6)); // Bitwise And Operator
        System.out.println((5|6)); //Bitwise Or Operator
        System.out.println((~5)); // Bitwise Not operator
        System.out.println((5<<2)); // bitwise left shift operator
        System.out.println((5>>2)); // bitwise right shift operator
    }
}
